def run_sequence(sequence_name):
    """运行指定的消息序列"""
    if sequence_name not in SEQUENCES:
        print(f"错误：未找到序列 '{sequence_name}'，可用的序列: {list(SEQUENCES.keys())}")
        return

    seq = SEQUENCES[sequence_name]
    app = QApplication.instance()
    if app is None:
        app = QApplication(sys.argv)

    hud = HudWindow()
    signals = get_hud_signals()
    signals.preview_text.connect(hud.on_preview_text)

    # Ctrl+C 退出支持
    def sigint_handler(signum, frame):
        print("\n收到 Ctrl+C，正在退出...")
        app.quit()
    signal.signal(signal.SIGINT, sigint_handler)

    # 可选：定时器保持 Python 信号处理活跃（Windows 下需要）
    keep_alive = QTimer()
    keep_alive.start(200)
    keep_alive.timeout.connect(lambda: None)

    # 计算绝对时间发送消息
    timeline = []
    current = 0
    for delay, msg in seq:
        current += delay
        timeline.append((current, msg))

    idx = 0
    def send_next():
        nonlocal idx
        if idx < len(timeline):
            delay_to_next = timeline[idx][0] - (timeline[idx-1][0] if idx > 0 else 0)
            # 使用单次定时器在正确时刻发送
            QTimer.singleShot(delay_to_next if idx > 0 else timeline[0][0], send_message)
        else:
            print("序列播放完毕。")

    def send_message():
        nonlocal idx
        if idx >= len(timeline):
            return
        _, msg = timeline[idx]
        print(f"[{timeline[idx][0]:>4}ms] 发射: '{msg}'")
        signals.preview_text.emit(msg)
        idx += 1
        # 调度下一条
        if idx < len(timeline):
            next_delay = timeline[idx][0] - timeline[idx-1][0]
            QTimer.singleShot(next_delay, send_message)

    # 启动序列
    QTimer.singleShot(0, send_next)

    # 如果还没有运行事件循环则启动
    if not app.startingUp():
        sys.exit(app.exec())
    else:
        app.exec()


